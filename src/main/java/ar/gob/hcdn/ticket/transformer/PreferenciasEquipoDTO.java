package ar.gob.hcdn.ticket.transformer;

public class PreferenciasEquipoDTO {
    private double terror;
    private double aventura;
    private double dificultad;

    public double getTerror() {
        return terror;
    }

    public void setTerror(double terror) {
        this.terror = terror;
    }

    public double getAventura() {
        return aventura;
    }

    public void setAventura(double aventura) {
        this.aventura = aventura;
    }

    public double getDificultad() {
        return dificultad;
    }

    public void setDificultad(double dificultad) {
        this.dificultad = dificultad;
    }
}
